#include "libft.h"

void	ft_bzero(void *s, size_t n)

{
	size_t	i;

	i = 0;
	if (!(char *)s)
	{
		return ;
	}
	while (n > 0)
	{
		((char *)s)[i++] = '\0';
		n--;
	}
}
